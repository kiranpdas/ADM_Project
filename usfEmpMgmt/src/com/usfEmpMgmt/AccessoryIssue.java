package com.usfEmpMgmt;

import java.util.Date;

public class AccessoryIssue {
 private String accessoryName;
 private String accessoryStatus;
 private Date accessoryIssueDate;
public String getAccessoryName() {
	return accessoryName;
}
public void setAccessoryName(String accessoryName) {
	this.accessoryName = accessoryName;
}
public String getAccessoryStatus() {
	return accessoryStatus;
}
public void setAccessoryStatus(String accessoryStatus) {
	this.accessoryStatus = accessoryStatus;
}
public Date getAccessoryIssueDate() {
	return accessoryIssueDate;
}
public void setAccessoryIssueDate(Date accessoryIssueDate) {
	this.accessoryIssueDate = accessoryIssueDate;
}
 
 
}
